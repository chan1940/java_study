public class Calculator {

    public double add(double m, double n) {
        return m + n;
    }

    public double minus(double m, double n) {
        return m - n;
    }

    public double multiply(double m, double n) {
        return m * n;
    }

    public double divide(double m, double n) {
        return m / n;
    }


}
